"""Benchmark: EMLCompositeReward vs CARLReward vs PhaseAdaptiveCARLReward.

Generates 100 synthetic CoherenceTrace objects with realistic Phi/ms/cq/defect
distributions, runs all three reward heads, and reports mean/std/latency and
the correlation between EML and Phase-Adaptive.

Output: scripts/eml_reward_benchmark.md
"""
from __future__ import annotations

import time
from pathlib import Path

import numpy as np

from carl_core.coherence_trace import CoherenceTrace

from carl_studio.training.rewards import (
    CARLReward,
    PhaseAdaptiveCARLReward,
    make_eml_reward,
)


N_TRACES: int = 100
T_TOKENS: int = 32
V_VOCAB: int = 64


def _synth_trace(seed: int) -> CoherenceTrace:
    """Build one realistic-ish CoherenceTrace. Logits are Gaussian; vocab is
    modestly sized so cloud_quality and phi both span their full ranges across
    the sample set."""
    rng = np.random.default_rng(seed)
    # Vary the sharpness so we get a mix of ordered (low-entropy) and disordered
    # (high-entropy) traces -> all three rewards see representative inputs.
    sharpness = rng.uniform(0.3, 3.0)
    logits = sharpness * rng.standard_normal((T_TOKENS, V_VOCAB))
    token_ids = rng.integers(0, V_VOCAB, size=T_TOKENS)
    return CoherenceTrace.from_logits(logits, token_ids)


def _time_reward(fn: object, traces: list[CoherenceTrace]) -> tuple[list[float], float]:
    """Call reward on every trace, return (rewards, mean_latency_us)."""
    rewards: list[float] = []
    # warm-up
    fn.score_from_trace(traces[0])  # type: ignore[attr-defined]
    t0 = time.perf_counter_ns()
    for tr in traces:
        s, _ = fn.score_from_trace(tr)  # type: ignore[attr-defined]
        rewards.append(s)
    t1 = time.perf_counter_ns()
    mean_us = (t1 - t0) / 1e3 / len(traces)
    return rewards, mean_us


def _pearson(a: list[float], b: list[float]) -> float:
    """Pearson correlation; returns NaN if either side is constant."""
    av = np.asarray(a, dtype=np.float64)
    bv = np.asarray(b, dtype=np.float64)
    if np.std(av) < 1e-12 or np.std(bv) < 1e-12:
        return float("nan")
    return float(np.corrcoef(av, bv)[0, 1])


def main() -> None:
    print(f"Generating {N_TRACES} synthetic traces ...")
    traces = [_synth_trace(seed=s) for s in range(N_TRACES)]

    carl = CARLReward()
    pa = PhaseAdaptiveCARLReward()
    eml = make_eml_reward()

    print("Timing reward heads ...")
    carl_r, carl_us = _time_reward(carl, traces)
    pa_r, pa_us = _time_reward(pa, traces)
    eml_r, eml_us = _time_reward(eml, traces)

    stats = {
        "CARLReward": (carl_r, carl_us),
        "PhaseAdaptiveCARLReward": (pa_r, pa_us),
        "EMLCompositeReward": (eml_r, eml_us),
    }

    print()
    print(f"{'head':<28} {'mean':>8} {'std':>8} {'min':>8} {'max':>8} {'lat_us':>10}")
    for name, (rs, us) in stats.items():
        ra = np.asarray(rs, dtype=np.float64)
        print(f"{name:<28} {ra.mean():>8.4f} {ra.std():>8.4f} "
              f"{ra.min():>8.4f} {ra.max():>8.4f} {us:>10.2f}")

    corr_eml_pa = _pearson(eml_r, pa_r)
    corr_eml_carl = _pearson(eml_r, carl_r)
    corr_pa_carl = _pearson(pa_r, carl_r)
    print()
    print(f"corr(EML, Phase-Adaptive):  {corr_eml_pa:.4f}")
    print(f"corr(EML, CARL):            {corr_eml_carl:.4f}")
    print(f"corr(Phase-Adaptive, CARL): {corr_pa_carl:.4f}")

    # Write the markdown summary.
    out_path = Path(__file__).parent / "eml_reward_benchmark.md"
    lines: list[str] = [
        "# EML reward head benchmark",
        "",
        "Generated by `scripts/benchmark_eml_reward.py`. Compares the three "
        "reward-head options available via `reward_class`.",
        "",
        f"- n traces: {N_TRACES}",
        f"- tokens per trace: {T_TOKENS}",
        f"- vocab size: {V_VOCAB}",
        "",
        "## Summary table",
        "",
        "| reward head | mean | std | min | max | latency (us) |",
        "| --- | ---: | ---: | ---: | ---: | ---: |",
    ]
    for name, (rs, us) in stats.items():
        ra = np.asarray(rs, dtype=np.float64)
        lines.append(
            f"| `{name}` | {ra.mean():.4f} | {ra.std():.4f} | "
            f"{ra.min():.4f} | {ra.max():.4f} | {us:.2f} |"
        )
    lines += [
        "",
        "## Correlations",
        "",
        f"- corr(EML, Phase-Adaptive): **{corr_eml_pa:.4f}**",
        f"- corr(EML, CARL): **{corr_eml_carl:.4f}**",
        f"- corr(Phase-Adaptive, CARL): **{corr_pa_carl:.4f}**",
        "",
        "## Notes",
        "",
        "- `EMLCompositeReward` is the depth-3 learnable EML tree (T2). "
        "Leaf CONST params start at 0; Adam can adapt them on-policy.",
        "- Latency is wall-clock average across all traces on a warm run.",
        "- `PhaseAdaptiveCARLReward` caches the last trace on every score "
        "call, so its first call in this benchmark runs in the static "
        "regime; subsequent calls reflect the most recent Kuramoto-R.",
    ]
    out_path.write_text("\n".join(lines) + "\n")
    print()
    print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()
